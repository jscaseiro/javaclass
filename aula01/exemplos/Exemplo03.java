package exemplos;

public class Exemplo03 {
   public static void main(String[] args) {
       // Sintaxe: [TIPO] [NOME, NOME2, NOME3, ...];
       int numero; //declaração/criação de variável
       numero = 10; //atribuição de um valor à variável
       System.out.println(numero); 
       numero = 5; //sobrescreve
       System.out.println(numero); 
       System.out.println(numero + 5); 
       System.out.println(numero);
       // Para guardar o valor 
       numero = numero + 5; 
       System.out.println(numero); 
   } 
}
