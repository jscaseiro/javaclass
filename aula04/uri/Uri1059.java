/**
 * 1059 - Even Numbers
 */
public class Uri1059 {

    public static void main(String[] args) {

        for (int evenNro = 1; evenNro <= 100; evenNro++) {
            
            if (evenNro % 2 == 0) {
                System.out.println(evenNro);
            }
            
        }
    }
}