package exercicios;

public class Exercicio01 {
    public static void main(String[] args) {
        System.out.println("15 + 7 = " + (15 + 7)); // O interpretador assume tudo com o primeiro termo.
        System.out.println("15 * 7 = " + (15 * 7)); // O interpretador assume tudo com o primeiro termo.
        System.out.println("15 / 7 = " + (15.0 / 7)); // O interpretador assume tudo com o primeiro termo.
        System.out.println("15 - 7 = " + (15 - 7)); // O interpretador assume tudo com o primeiro termo.
    }    
}
